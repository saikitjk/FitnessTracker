const express = require("express");
const router = express.router();
const db = require("../model");
const Workout = require("../models/workout.js");

router.get("/api/workouts", (req, res) => {
  db.Workout.find()
    .then((dbWorkout) => {
      res.json(dbWorkout);
    })
    .catch((err) => {
      res.json(err);
    });
});

module.exports = router;

